
// abrir e fechar janela quem somos
function openNewScreen() {
    var newScreen = document.getElementById("new-screen");
    newScreen.classList.remove("hidden");
    setTimeout(function() {
      newScreen.classList.add("open");
    }, 10);
  }
  
  function closeNewScreen() {
    var newScreen = document.getElementById("new-screen");
    newScreen.classList.remove("open");
    setTimeout(function() {
      newScreen.classList.add("hidden");
    }, 500);
  }

